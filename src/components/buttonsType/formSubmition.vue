      </div>
    </div>

 







  </div>
</template>

<script>
export default {
 data () {
    return {
      test: '',
      submitting: false
    }
  },

  methods: {
    simulateSubmit () {
      this.submitting = true

      // Simulating a delay here.
      // When we are done, we reset "submitting"
      // Boolean to false to restore the
      // initial state.
      setTimeout(() => {
        // delay simulated, we are done,
        // now restoring submit to its initial state
        this.submitting = false
      }, 3000)
    }
  }
    
}
</script>
